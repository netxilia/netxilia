package org.netxilia.api.reference;

public class MutableRange {
	private int min;
	private int max;

	public MutableRange(int min) {
		this.min = min;
		this.max = min + 1;
	}

	public MutableRange(int min, int max) {
		this.min = min;
		this.max = max;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public Range toRange() {
		return Range.range(min, max);
	}

}
